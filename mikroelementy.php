            <br>

            <h3>Cynk (Zn)</h3>
            <p><b>Rola:</b> wspomaga odporność organizmu, konieczny do przemiany węglowodanów i lipidów, 
            uczestniczy w pracy mięśni, poprawia sprawność wysiłkową organizmu, 
            wpływa na funkcjonowanie jajników u klaczy jest składnikiem antyoksydantów, wpływa na krzepliwość krwi, gojenie ran,</p>


            <p><b>Objawy niedoborów:</b> słaby apetyt, problemy skórne, strupki i zapalenia mieszków włosowych, 
