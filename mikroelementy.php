            <p><b>Źródła:</b> spirulina,</p>

            <p><b>Objawy niedoborów:</b> choroby kości, problemy z płodnością, brak koordynacji ruchowej u młodych koni, osteochondroza.</p>
            <br>

            <h3>Jod (I)</h3>
            <p><b>Rola:</b> synteza tyroksyny wpływającej na metabolizm organizmu, 
