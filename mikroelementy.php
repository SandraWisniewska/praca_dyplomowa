            <h3>Miedź (Cu)</h3>
            <p><b>Rola:</b> przeciwdziała anemii, bierze udział w syntezie hemoglobiny, 
            wspomaga poprawny wzrost kości, chrząstek, i sierści, potrzebna do wytwarzania kolagenu, 
            uczestniczy w procesie syntezy melaniny przez co ma wpływ na kolor okrywy włosowej, 
            wpływa na funkcjonowanie układu nerwowego.</p>


            <p><b>Objawy niedoborów:</b> słaby wzrost, osteochondroza, problemy skórne, 
