            <br>
            <p>- <b>konie z chorobą wrzodową (EGUS)</b> – zarówno aktywny stan chorobowy, 
                jak i zaleczony i skłonność do powstawania wrzodów żołądka determinuje konieczność wprowadzenia specjalnej diety. 
                Podawanie lucerny jest jednym z najczęściej zalecanych komponentów w takich sytuacjach.</p>
            <br>
            <p>- <b>klacze źrebne i laktujące</b> – wielu właścicieli koni boryka się z problemem chudnięcia u klaczy w ciąży i karmiących. 
                Czasem powodem takiego stanu rzeczy jest zbyt mała podaż energii, ale najczęściej powodem jest właśnie ilość i jakość białka, 
                które pobiera klacz. 
                Oczywiście problem może być bardziej złożony, ale wprowadzenie lucerny do jadłospisu takiej klaczy 
                jest stosunkowo bezpiecznym pierwszym krokiem w kierunku ulepszenia jej diety.</p>
