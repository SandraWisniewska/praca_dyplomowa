            <h3><b>Jakim koniom podawać lucernę?</b></h3>
            <br>
            <p>- <b>konie rosnące</b> – z uwagi na wysoką jakość białka w szczególności konie rosnące intensywnie, 
            czyli do 2 lat, potrzebują dużych ilości aminokwasów dostarczanych w pożywieniu, 
            gdyż są one budulcem praktycznie wszystkich tkanek w organizmie. 
            Jednak pamiętać należy, że koń dość aktywnie rośnie do 5’tego (a nawet 6’stego) roku życia 
            i w tym czasie jego żywienie także powinno być bilansowane bardzo uważnie. 
            Tym bardziej jeśli koń pracuje,</p>
            <br>
            <p>- <b>konie pracujące</b> – ich zapotrzebowanie na wysokiej jakości białko 
